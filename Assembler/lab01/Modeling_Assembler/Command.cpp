#include "Command.h"

int Command::multiplication(const int& a, const int& b)
{
	return a * b;
}

int Command::addition(const int& a, const int& b)
{
	return a + b;
}

int Command::division(const int& a, const int& b)
{
	return a / b;
}

int Command::subtraction(const int& a, const int& b)
{
	return a - b;
}